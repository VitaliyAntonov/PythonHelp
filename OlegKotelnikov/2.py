# логическая цепочка if else в одну строку
def f(x): return 1-(x+2)**2 if x<=-2 else -x/2 if -2<x<=2 else (x-2)**2+1
print(f(4))


# работа со словарем, ключ - оператор сравнения
def d(x):
    return {
        x <= -2:      1 - (x + 2) ** 2,
        -2 < x <= 2:  -x / 2,
        2 < x:        (x - 2) ** 2 + 1
    }[True]
print(d(4))